#include "push_swap.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	void	*array;

	array = malloc(nmemb * size);
	if (!array)
		return (0);
	ft_memset(array, 0, nmemb * size);
	return (array);
}
